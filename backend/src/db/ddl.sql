-- ────────────────────────────────────────────────────────────────────────────
-- SUPABASE AUTH HOOK
-- ────────────────────────────────────────────────────────────────────────────
-- When a user registers via Supabase Auth, this function automatically creates
-- their row in the public.users table. Set this as an Auth Hook in:
-- Supabase Dashboard → Authentication → Hooks → "After user created"
--
-- Alternatively call it from your register endpoint via supabase.auth.signUp()
-- and then insert into public.users with the returned UUID.
--
-- Function is provided here for reference — wire it up in the dashboard.

CREATE OR REPLACE FUNCTION public.handle_new_auth_user()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  INSERT INTO public.users (user_id, full_name, email, password, role)
  VALUES (
    NEW.id,                                           -- UUID from auth.users
    COALESCE(NEW.raw_user_meta_data->>'full_name', 'User'),
    NEW.email,
    '',                                               -- password managed by Supabase Auth
    COALESCE(NEW.raw_user_meta_data->>'role', 'student')
  )
  ON CONFLICT (user_id) DO NOTHING;
  RETURN NEW;
END;
$$;

-- Wire trigger on auth.users (requires elevated privileges in some Supabase setups).
-- If permissions are insufficient, skip gracefully and use Dashboard auth hook instead.
DO $$
BEGIN
  BEGIN
    DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
    CREATE TRIGGER on_auth_user_created
      AFTER INSERT ON auth.users
      FOR EACH ROW EXECUTE FUNCTION public.handle_new_auth_user();
  EXCEPTION
    WHEN insufficient_privilege THEN
      RAISE NOTICE 'Skipping auth.users trigger creation due to insufficient privileges. Configure Authentication > Hooks > After user created to call public.handle_new_auth_user().';
  END;
END;
$$;

